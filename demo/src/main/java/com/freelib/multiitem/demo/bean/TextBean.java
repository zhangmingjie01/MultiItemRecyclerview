package com.freelib.multiitem.demo.bean;

/**
 * 文本bean
 * Created by free46000 on 2017/3/19.
 */
public class TextBean {
    private String text;

    public TextBean(String text) {
        this.text = text;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    @Override
    public String toString() {
        return text;
    }
}
